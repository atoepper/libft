#include "libft.h"

long	ft_hextoi(char *s, int i)
{
	long	n;

	n = 0;
	while (*s && i > 0)
	{
		n *= 16;
		if (*s >= 'A' && *s <= 'F')
			n += *s - 'A' + 10;
		else if (*s >= 'a' && *s <= 'f')
			n += *s - 'a' + 10;
		else
			n += *s - '0';
		++s;
		i--;
	}
	return (n);
}
